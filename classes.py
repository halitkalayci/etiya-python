class Math:
    def sum(self,a,b):
        return a+b
    def multiply(self,a,b):
        return a*b

# nesne ve obje farkı => 
mathLibrary = Math() # instance oluşturma => obje oluşturma
sum = mathLibrary.sum(10,20)
multiply = mathLibrary.multiply(10,20)

print(sum)
print(multiply)

